function setLanguage(lang) {
  const dictionary = translations[lang] || translations.pl;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.getAttribute("data-lang") === lang);
  });

  localStorage.setItem("speedHanysLang", lang);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.getAttribute("data-lang"));
  });
});

const savedLanguage = localStorage.getItem("speedHanysLang") || "pl";
setLanguage(savedLanguage);

const calculateBtn = document.getElementById("calculateBtn");

function calculatePrice() {
  const distance = Number(document.getElementById("distance")?.value || 0);
  const serviceType = document.getElementById("serviceType")?.value || "standard";
  const help = document.getElementById("help")?.value || "no";
  const floor = Number(document.getElementById("floor")?.value || 0);

  let price = 100;

  price += distance * 2.5;

  if (serviceType === "moving") price += 160;
  if (serviceType === "construction") price += 120;
  if (serviceType === "vehicle") price += 180;

  if (help === "yes") price += 100;

  price += floor;

  price = Math.max(100, Math.round(price / 10) * 10);

  const result = document.getElementById("resultPrice");
  if (result) {
    result.textContent = price;
  }
}

if (calculateBtn) {
  calculateBtn.addEventListener("click", calculatePrice);
  calculatePrice();
}
const heroVideo = document.querySelector(".hero-video");

function startHeroVideo() {
  if (!heroVideo) return;

  heroVideo.muted = true;
  heroVideo.defaultMuted = true;
  heroVideo.playsInline = true;

  heroVideo.setAttribute("muted", "");
  heroVideo.setAttribute("playsinline", "");
  heroVideo.setAttribute("webkit-playsinline", "");

  heroVideo.play().catch(() => {});
}

document.addEventListener("DOMContentLoaded", startHeroVideo);
window.addEventListener("load", startHeroVideo);
document.addEventListener("touchstart", startHeroVideo, { once: true });
document.addEventListener("click", startHeroVideo, { once: true });
document.addEventListener("scroll", startHeroVideo, { once: true });
